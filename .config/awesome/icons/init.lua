local function set_icon(icon_name)
    local i = p .. icon_name .. ".png"
    icons[icon_name] = i
end

function icons_init(icon_set)
    -- Set path to icons
    p = gears.filesystem.get_configuration_dir() .. "icons/" .. icon_set .. "/"

    for i = 1, #icon_names do
      set_icon(icon_names[i])
    end
end

return icons

